    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
           String id= txtidupdate.getText();
           String question=txtquestionupdate.getText();
           String opt1=txtchoice1update.getText();
           String opt2=txtchoice2update.getText();
           String opt3=txtchoice3update.getText();
           String opt4=txtchoice4update.getText();
           String answer=txtanswerupdate.getText();
           try{
             Connection con=Projectconnectionprovider.getConnection();
             PreparedStatement ps=con.prepareStatement("UPDATE questions SET questions=?,Option1=?,Option2=?,Option3=?,Option4=?,answer=? WHERE ID=?");
             ps.setString(1, question);
             ps.setString(2, opt1);
             ps.setString(3, opt2);
             ps.setString(4, opt3);
             ps.setString(5, opt4);
             ps.setString(6, answer);
             ps.setString(7, id);
             ps.executeUpdate();
             JFrame jf=new JFrame();
             jf.setAlwaysOnTop(true);
           JOptionPane.showMessageDialog(jf,"Successufully Updated");
           setVisible(false);
           new UpdateQuestion().setVisible(true);
           }
           catch(Exception ex){
           JFrame jf=new JFrame();
           jf.setAlwaysOnTop(true);
           JOptionPane.showMessageDialog(jf,ex);
           }
    }//GEN-LAST:event_btnupdateActionPerformed

