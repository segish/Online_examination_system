    private void btnsearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsearchMouseClicked
        String id=txtidupdate.getText();
        try{
            Connection cn=Projectconnectionprovider.getConnection();
            String query=("select*from questions where ID='"+id+"'");
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(query);
            if(rs.next()){
                 txtquestionupdate.setText(rs.getString(3));
                 txtchoice1update.setText(rs.getString(4));
                 txtchoice2update.setText(rs.getString(5));
                 txtchoice3update.setText(rs.getString(6));
                 txtchoice4update.setText(rs.getString(7));
                 txtanswerupdate.setText(rs.getString(8));
                 txtidupdate.setEditable(false);
            }
            else{
           JFrame jf=new JFrame();
           jf.setAlwaysOnTop(true);
           JOptionPane.showMessageDialog(jf,"Question ID dosen't Exist");
            }
        }
        catch(Exception ex){
            JFrame jf=new JFrame();
           jf.setAlwaysOnTop(true);
           JOptionPane.showMessageDialog(jf,ex);
        }
    }//GEN-LAST:event_btnsearchMouseClicked

